    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        String[] operacion = jTextField1.getText().split(" ");
        if (operacion[1].equals("+")) {
            jTextField1.setText(String.valueOf(WebServiceClient.suma(Double.valueOf(operacion[0]), Double.valueOf(operacion[2]))));
        }
        if (operacion[1].equals("-")) {
            jTextField1.setText(String.valueOf(WebServiceClient.resta(Double.valueOf(operacion[0]), Double.valueOf(operacion[2]))));
        }
        if (operacion[1].equals("*")) {
            jTextField1.setText(String.valueOf(WebServiceClient.multiplicacion(Double.valueOf(operacion[0]), Double.valueOf(operacion[2]))));
        }
        if (operacion[1].equals("/")) {
            jTextField1.setText(String.valueOf(WebServiceClient.division(Double.valueOf(operacion[0]), Double.valueOf(operacion[2]))));
        }
        if (operacion[1].equals("^")) {
            jTextField1.setText(String.valueOf(WebServiceClient.potencia(Double.valueOf(operacion[0]), Double.valueOf(operacion[2]))));
        }
    }//GEN-LAST:event_jButton18ActionPerformed

